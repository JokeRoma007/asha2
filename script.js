// --- App Lifecycle Initialization ---
document.addEventListener("DOMContentLoaded", () => {
    initSmoothScroll();
    initCursor();
    initAnimations();
    initLanguageSwitcher();
    initAccordion();
    initHeaderEffect();
    initMobileMenu();
    
    const savedLang = localStorage.getItem("preferred_lang") || "cs";
    updateLanguage(savedLang);
    
    const activeBtn = document.querySelector(`.lang-btn[data-lang="${savedLang}"]`);
    if (activeBtn) {
        document.querySelectorAll(".lang-btn").forEach(b => b.classList.remove("active"));
        activeBtn.classList.add("active");
    }
});

// --- Lenis Smooth Scrolling ---
function initSmoothScroll() {
    const lenis = new Lenis({
        duration: 1.2,
        easing: (t) => Math.min(1, 1.001 - Math.pow(2, -10 * t)),
        orientation: 'vertical',
        gestureOrientation: 'vertical',
        smoothWheel: true
    });
    function raf(time) {
        lenis.raf(time);
        requestAnimationFrame(raf);
    }
    requestAnimationFrame(raf);
}

// --- Custom Cursor ---
function initCursor() {
    const cursor = document.querySelector(".custom-cursor");
    const dot = document.querySelector(".custom-cursor-dot");
    if (!cursor || !dot) return;
    
    let mouseX = 0, mouseY = 0;
    let cursorX = 0, cursorY = 0;
    
    document.addEventListener("mousemove", (e) => {
        mouseX = e.clientX;
        mouseY = e.clientY;
        dot.style.left = mouseX + "px";
        dot.style.top = mouseY + "px";
    });
    
    function render() {
        cursorX += (mouseX - cursorX) * 0.15;
        cursorY += (mouseY - cursorY) * 0.15;
        cursor.style.left = cursorX + "px";
        cursor.style.top = cursorY + "px";
        requestAnimationFrame(render);
    }
    render();
    
    document.addEventListener("mouseleave", () => {
        cursor.style.opacity = "0";
        dot.style.opacity = "0";
    });
    document.addEventListener("mouseenter", () => {
        cursor.style.opacity = "1";
        dot.style.opacity = "1";
    });
    
    const interactive = "a, button, [onclick], .accordion-header, .lang-btn, .btn-primary, .btn-secondary, .btn-link, .premium-gallery-item";
    document.addEventListener("mouseover", (e) => {
        if (e.target.closest(interactive)) {
            cursor.style.width = "60px";
            cursor.style.height = "60px";
            cursor.style.backgroundColor = "rgba(230, 225, 218, 0.05)";
            cursor.style.borderColor = "var(--accent-color)";
        }
    });
    document.addEventListener("mouseout", (e) => {
        if (e.target.closest(interactive)) {
            cursor.style.width = "30px";
            cursor.style.height = "30px";
            cursor.style.backgroundColor = "transparent";
            cursor.style.borderColor = "var(--accent-color)";
        }
    });
}

// --- GSAP Animations ---
function initAnimations() {
    gsap.registerPlugin(ScrollTrigger);
    
    if(document.querySelector(".hero-tagline")) {
        const tl = gsap.timeline();
        tl.from(".hero-tagline", { opacity: 0, y: 20, duration: 0.8, ease: "power3.out", delay: 0.2 })
          .from(".reveal-text", { y: "100%", opacity: 0, duration: 1, stagger: 0.15, ease: "power4.out" }, "-=0.6")
          .from(".hero-subtitle", { opacity: 0, y: 15, duration: 0.8 }, "-=0.5")
          .from(".hero-actions", { opacity: 0, y: 15, duration: 0.8 }, "-=0.6")
          .from(".hero-image-wrapper", { opacity: 0, scale: 0.95, duration: 1.2, ease: "power2.out" }, "-=1.0");
    }
    
    const revealElements = document.querySelectorAll(".animate-on-scroll");
    revealElements.forEach(element => {
        gsap.from(element, {
            scrollTrigger: { trigger: element, start: "top 85%", toggleActions: "play none none none" },
            opacity: 0,
            y: 40,
            duration: 1,
            ease: "power3.out"
        });
    });
}

// --- Language Switcher ---
function initLanguageSwitcher() {
    const buttons = document.querySelectorAll(".lang-btn");
    buttons.forEach(btn => {
        btn.addEventListener("click", (e) => {
            const lang = e.target.getAttribute("data-lang");
            buttons.forEach(b => b.classList.remove("active"));
            e.target.classList.add("active");
            localStorage.setItem("preferred_lang", lang);
            updateLanguage(lang);
        });
    });
}

function updateLanguage(lang) {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });
    document.documentElement.lang = lang === 'ua' ? 'uk' : lang;
}

// --- FAQ Accordion ---
function initAccordion() {
    const headers = document.querySelectorAll(".accordion-header");
    headers.forEach(header => {
        header.addEventListener("click", function() {
            const item = this.parentElement;
            const content = this.nextElementSibling;
            item.classList.toggle("active");
            content.style.maxHeight = item.classList.contains("active") ? content.scrollHeight + "px" : "0px";
        });
    });
}

// --- Header Scroll Effect ---
function initHeaderEffect() {
    const header = document.querySelector(".header");
    window.addEventListener("scroll", () => {
        if (window.scrollY > 50) header.classList.add("scrolled");
        else header.classList.remove("scrolled");
    });
}

// --- Mobile Menu ---
function initMobileMenu() {
    const toggle = document.querySelector(".menu-toggle");
    const menu = document.querySelector(".nav-menu");
    const closeBtn = document.querySelector(".mobile-menu-close");
    const links = document.querySelectorAll(".nav-menu a");
    if (!toggle || !menu) return;

    function openMenu() { 
        menu.classList.add("active"); 
        document.body.style.overflow = "hidden"; 
    }
    function closeMenu() { 
        menu.classList.remove("active"); 
        document.body.style.overflow = ""; 
    }

    toggle.addEventListener("click", openMenu);
    if (closeBtn) closeBtn.addEventListener("click", closeMenu);
    links.forEach(link => link.addEventListener("click", closeMenu));
}